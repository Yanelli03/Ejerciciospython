# Operadores Matemáticos

name = 'Frank' # asignacion =

print(20 + 30) # suma +
print(20 - 15) # resta -
print(12 * 41) # multiplicación *
print(12 / 3)  # división flotante /
print(13 // 4) # división entera //
print(13 % 3) # modulo %
print(2 ** 5) # potencia **

# Operadores de comparación
print(10 < 10) # menor que
print(10 > 10) # mayor que
print(10 <= 10) # menor o igual que
print(10 >= 10) # mayor o igual que
print(10 == 10) # igual
print(10 != 10) # diferente
 
# Operadores Lógicos
print(True and True) #AND
print(True or False) # OR
print(not True) #NOT