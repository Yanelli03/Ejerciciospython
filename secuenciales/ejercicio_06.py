# Calcular la media de tres números pedidos por teclado

print('Calcular Media')

número_1 = int (input('Ingresa número 1: '))
número_2 = int (input('Ingresa número 2: '))
número_3 = int (input('Ingresa número 3: '))
número_4 = int (input('Ingresa número 4: '))
número_5 = int (input('Ingresa número 5: '))

Media = (número_1 + número_2 + número_3 + número_4 + número_5) / 5

print('La Media es:', Media)
